public class RelationalOperator{
	public static void main(String args[]){
		int num1=30;
		int num2=20;
		/*char num1='f';
		char num2='f';*/
		System.out.println(num1>num2);
		System.out.println(num1<num2);
		System.out.println(num1<=num2);
		System.out.println(num1>=num2);
		System.out.println(num1==num2);
		System.out.println(num1!=num2);
	}
}


