//Program using Assignment and Logical operators

public class OperatorsDemo{
	public static void main(String args[]){
		int num1=10;
		num1+=20;
		System.out.println(num1);
		num1-=10;
		System.out.println(num1);
		num1*=2;
		System.out.println(num1);
		num1/=5;
		System.out.println(num1);
		num1%=5;
		System.out.println(num1);
}
}


















